import java.util.*;
public class Recursion01{
    public static void main(String[] args){
        System.out.println("Hello World");
        print1();
    }
    static void print1(){
        System.out.println("Hello World");
        print2();
    }
    static void print2(){
        System.out.println("Hello World");
        print3();
    }
    static void print3(){
        System.out.println("Hello World");
        print4();
    }
    static void print4(){
        System.out.println("Hello World");
    }
    
}